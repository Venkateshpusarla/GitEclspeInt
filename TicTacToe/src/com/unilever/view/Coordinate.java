package com.unilever.view;

public class Coordinate {
	private Integer xAxis;
	private Integer yAxis;
	
	public Coordinate(Integer xAxis, Integer yAxis) {
		this.xAxis = xAxis;
		this.yAxis = yAxis;
	}
	public Integer getxAxis() {
		return xAxis;
	}
	public Integer getyAxis() {
		return yAxis;
	}
}
